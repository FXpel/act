
public class Point {
	private int x_i;
	private int y_i;
	
	public Point(int x, int y) {
		this.setX_i(x);
		this.setY_i(y);
	}

	/**
	 * @return the x_i
	 */
	public int getX_i() {
		return x_i;
	}

	/**
	 * @param x_i the x_i to set
	 */
	public void setX_i(int x_i) {
		this.x_i = x_i;
	}

	/**
	 * @return the y_i
	 */
	public int getY_i() {
		return y_i;
	}

	/**
	 * @param y_i the y_i to set
	 */
	public void setY_i(int y_i) {
		this.y_i = y_i;
	}
}
